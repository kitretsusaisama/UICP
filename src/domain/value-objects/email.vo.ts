export class Email {
  private constructor(private readonly value: string) {}

  static create(raw: string): Email {
    if (typeof raw !== 'string') {
      throw new DomainException(DomainErrorCode.INVALID_EMAIL, 'Email must be a string');
    }

    const normalized = raw.trim().toLowerCase();

    if (normalized.length > MAX_EMAIL_LENGTH) {
      throw new DomainException(
        DomainErrorCode.INVALID_EMAIL,
        `Email exceeds maximum length of ${MAX_EMAIL_LENGTH} characters`,
      );
    }

    if (!EMAIL_REGEX.test(normalized)) {
      throw new DomainException(
        DomainErrorCode.INVALID_EMAIL,
        `Email does not match RFC 5322 format: ${normalized}`,
      );
    }

    const domain = normalized.split('@')[1]!;

    if (DISPOSABLE_DOMAINS.has(domain)) {
      throw new DomainException(
        DomainErrorCode.DISPOSABLE_EMAIL_DOMAIN,
        `Disposable email domain not allowed: ${domain}`,
      );
    }

    return new Email(normalized);
  }

  getValue(): string {
    return this.value;
  }

  getDomain(): string {
    return this.value.split('@')[1]!;
  }

  /** Returns the normalized email for use as HMAC input (searchable encryption). */
  toHmacInput(): string {
    return this.value;
  }

  toString(): string {
    return this.value;
  }
}
